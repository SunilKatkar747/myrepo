
public class Swapping_variable {

	public static void main(String[] args) {
		int temp;
		
		int x=5;
		int y=7;
		System.out.println("before swapping value of the x="+x+ " value of y="+y);
		
		temp=x;
		x=y;
		y=temp;
		
		System.out.println( " after swapping value of the x="+x+ " value of y="+y);
		
		

	}

}
